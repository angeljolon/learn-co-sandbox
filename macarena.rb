#directions for dancing the macarena

def macarena
  puts "right arm out!"
  puts "left arm out!"
  puts "turn right arm over!"
  puts "turn left arm over"
  puts "right arm to left shoulder"
  puts "left arm to right shoulder"
  puts "right arm to head"
  puts "left arm to head"
  puts "right arm to left hip"
  puts "left arm to right hip"
  puts "right arm on right hip"
  puts "left arm on right hip"
  puts "swing hips!"
  puts "turn left"
end

macarena
puts "Ayyyyy Macarena!"
macarena
puts "Ayyyyy Macarena!"
macarena
puts "Ayyyyy Macarena!"
macarena
puts "End."

